// Regenerates src/spHistory.ts from the git log: one point per release commit
// (those whose subject carries a "(SP:n)" tag), with the commit's real date/time,
// its story points, and version. Powers the "SP over time" graph in Settings →
// Version history. Re-run after adding releases:
//
//   node scripts/gen-sp-history.cjs
//
const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

// Oldest → newest. Tab-separated ISO datetime + subject.
const raw = execSync('git log --reverse --pretty="%aI%x09%s"', { encoding: "utf8", maxBuffer: 64 * 1024 * 1024 });

const points = [];
for (const line of raw.split("\n")) {
  if (!line) continue;
  const tab = line.indexOf("\t");
  if (tab < 0) continue;
  const date = line.slice(0, tab);
  const subject = line.slice(tab + 1);
  // CODE (SP:n) VERSION rest...  → capture SP and the version token.
  const m = subject.match(/\(SP:([0-9.]+)\)\s+(\S+)/);
  if (!m) continue;
  const sp = Number(m[1]);
  if (!Number.isFinite(sp)) continue;
  points.push({ date, sp, version: m[2] });
}

const out =
  "// AUTO-GENERATED by scripts/gen-sp-history.cjs — do not edit by hand.\n" +
  "// One point per release commit: its real date/time, story points and version.\n\n" +
  "export interface SpPoint {\n  /** Commit datetime, ISO-8601. */\n  date: string;\n  /** Story points for that release. */\n  sp: number;\n  /** Version string from the commit subject. */\n  version: string;\n}\n\n" +
  "export const SP_HISTORY: SpPoint[] = [\n" +
  points.map((p) => `  { date: ${JSON.stringify(p.date)}, sp: ${p.sp}, version: ${JSON.stringify(p.version)} },`).join("\n") +
  "\n];\n";

const target = path.join(__dirname, "..", "src", "spHistory.ts");
fs.writeFileSync(target, out, "utf8");
console.log(`Wrote ${points.length} points to ${target}`);
